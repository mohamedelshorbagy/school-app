const validator = {
    isEmail: function (email) {
        const pattern = new RegExp(/(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])/ig);
        return pattern.test(email);
    },
    isPhone: function (phone) {
        const pattern = new RegExp(/^[0-9]+$/ig);
        return pattern.test(phone);
    },
    hasOnlyChars: function (str) {
        const pattern = new RegExp(/^[A-Za-z]+$/ig);
        return pattern.test(str);
    },
    isEmpty: function (str) {
        return str === '';
    }
}


module.exports = validator;